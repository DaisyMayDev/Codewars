// Convert a Number to a String:
// We need a function that can transform a number into a string.

const num1 = 35;

function numberToString(num1) {
  return `${num1}`;
}

console.log(
  `The number ${numberToString(num1)} has been converted to a string.`
);

//Output to console ->  The number 35 has been converted to a string.
