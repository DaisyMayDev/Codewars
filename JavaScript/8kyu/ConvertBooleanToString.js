//Convert a Boolean to a String:
//Implement a function which convert the given boolean value into its string representation.

const b = true;

function booleanToString(b) {
  return `${b}`;
}
console.log(`The boolean value has been converted to the string - ${booleanToString(b)}.`);

//Output to Console ->  The boolean value has been converted to the string - true.
