//Reverse List Order:
//You will create a function that takes in a list and returns a list with the reverse order.

let list = [1, 2, 3, 4];

function reverseList(list) {
    return list.reverse();
}

console.log(`The array [${list}] reversed is [${reverseList(list)}]`);

//Output to Console  -> The array [1,2,3,4] reversed is [4,3,2,1]
