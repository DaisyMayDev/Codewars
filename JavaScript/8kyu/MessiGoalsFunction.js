//Messi Goals Function:
//Complete the function to return his total number of goals in all three leagues.

const laLigaGoals = 43;
const copaDelReyGoals = 10;
const championsLeagueGoals = 5;

function goals(laLigaGoals, copaDelReyGoals, championsLeagueGoals) {
  return laLigaGoals + copaDelReyGoals + championsLeagueGoals;
}

console.log(`The total number of goals Messi scored is: ${goals(laLigaGoals,copaDelReyGoals,championsLeagueGoals)}`);

// Output to Console ->  The total number of goals Messi scored is: 58
